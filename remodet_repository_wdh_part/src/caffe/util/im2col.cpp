/**
 * 该函数建议使用NEON/EIGEN进行加速
 * 最后的通道可以使用Packet进行数据复制
 */
template <typename Dtype>
void im2col_nhwc_cpu(const Dtype* data_im, const int channels,
    const int height, const int width, const int kernel_h, const int kernel_w,
    const int pad_h, const int pad_w,
    const int stride_h, const int stride_w,
    const int dilation_h, const int dilation_w,
    Dtype* data_col) {
  // 输出尺度
  const int output_h = (height + 2 * pad_h -
    (dilation_h * (kernel_h - 1) + 1)) / stride_h + 1;
  const int output_w = (width + 2 * pad_w -
    (dilation_w * (kernel_w - 1) + 1)) / stride_w + 1;
  // [H,W,D,D,Nc] -> [D,D,Nc,No] (Weigts) -> [H,W,No] (Outputs)
  // 输出第i行
  for (int i = 0; i < output_h; ++i) {
    // 起始行
    int start_h = i * stride_h - pad_h;
    for (int j = 0; j < output_w; ++j) {
      // 起始列
      int start_w = j * stride_w - pad_w;
      // 遍历kernel
      for (int kernel_row = 0; kernel_row < kernel_h; kernel_row++) {
        int input_row = start_h + kernel_row * dilation_h;
        for (int kernel_col = 0; kernel_col < kernel_w; kernel_col++) {
          int input_col = start_w + kernel_col * dilation_w;
          if (is_a_ge_zero_and_a_lt_b(input_row, height) && is_a_ge_zero_and_a_lt_b(input_col, width)) {
            const Dtype* data_base = data_im + (input_row * width + input_col) * channels;
            for (int c = channels; c; c--) {
              *(data_col++) = *(data_base++);
            }
          } else {
            for (int c = channels; c; c--) {
              *(data_col++) = 0;
            }
          }
        }
      }
    }
  }
}

